package Identidades;


public class Perrov2 {

    private String nombr;
    private String raza;
    private int edad;
    private int tamanio;

    public Perrov2() {
    }

    public Perrov2(String nombr, String raza, int edad, int tamanio) {
        this.nombr = nombr;
        this.raza = raza;
        this.edad = edad;
        this.tamanio = tamanio;
    }

    public String getNombr() {
        return nombr;
    }

    public void setNombr(String nombr) {
        this.nombr = nombr;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getTamanio() {
        return tamanio;
    }

 
    public void setTamanio(int tamanio) {
        this.tamanio = tamanio;
    }

}
